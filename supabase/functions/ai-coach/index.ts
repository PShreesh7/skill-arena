import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const CHAT_SYSTEM_PROMPT = `You are an expert AI coding coach. You help users with:
- Data Structures & Algorithms (DSA)
- System Design
- Code Reviews
- Career guidance in tech
- Programming concepts (Java, Python, JavaScript, C++, etc.)
- Frameworks like Spring Boot, React, Django, etc.
- Machine Learning, Data Analysis, and more

Be concise, helpful, and encouraging. Use markdown formatting for code snippets and explanations. When explaining code, provide examples.`;

const CODE_ANALYSIS_SYSTEM_PROMPT = `You are an expert code reviewer and analyzer. When given code, you must:
1. Identify bugs, mistakes, and potential issues
2. Suggest optimizations for time/space complexity
3. Recommend best practices and cleaner patterns
4. Point out edge cases not handled
5. Provide an improved version of the code

Format your response in clear sections using markdown:
## 🐛 Issues Found
## ⚡ Optimizations
## 💡 Tips & Best Practices
## ✅ Improved Code

Be specific and actionable in your feedback.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, mode } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = mode === "code-analysis" ? CODE_ANALYSIS_SYSTEM_PROMPT : CHAT_SYSTEM_PROMPT;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("ai-coach error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
